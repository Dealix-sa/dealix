def rank_verticals() -> list[VerticalScore]:
    """Return SAUDI_VERTICALS sorted descending by total_score.

    Examples:
        >>> ranked = rank_verticals()
        >>> len(ranked)
        15
        >>> ranked[0].total_score >= ranked[-1].total_score
        True
    """
    return sorted(SAUDI_VERTICALS, key=lambda v: v.total_score, reverse=True)


def top_wedge() -> VerticalScore:
    """Return the highest-scoring sector — the recommended entry wedge.

    Examples:
        >>> w = top_wedge()
        >>> isinstance(w.sector, str)
        True
        >>> w.total_score >= 80
        True
    """
    return rank_verticals()[0]


