"""15-sector Saudi market vertical scoring for the Dealix launch OS.

Each VerticalScore reflects:
  revenue_potential  0-25   SAR market size + deal density
  pain_clarity       0-25   how well-defined the problem is
  regulatory_ease    0-20   low bureaucracy friction
  ai_readiness       0-15   existing digitisation level
  competition_gap    0-15   absence of strong incumbents

Total 0-100.  Scores are pre-researched; they are not generated by an LLM.
"""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class VerticalScore:
    """Scored representation of one Saudi market sector.

    Examples:
        >>> v = VerticalScore(
        ...     sector="automotive",
        ...     revenue_potential=22,
        ...     pain_clarity=22,
        ...     regulatory_ease=16,
        ...     ai_readiness=14,
        ...     competition_gap=12,
        ...     notes_ar="قطاع السيارات يعاني من ضعف متابعة العملاء المحتملين",
        ...     notes_en="High lead volume with poor follow-up discipline",
        ... )
        >>> v.total_score
        86
    """

    sector: str
    revenue_potential: int
    pain_clarity: int
    regulatory_ease: int
    ai_readiness: int
    competition_gap: int
    notes_ar: str = ""
    notes_en: str = ""

    @property
    def total_score(self) -> int:
        return (
            self.revenue_potential
            + self.pain_clarity
            + self.regulatory_ease
            + self.ai_readiness
            + self.competition_gap
        )


